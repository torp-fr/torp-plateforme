/**
 * PHASE 41: PASSIVE MODE - Index chunks for knowledge base (verification only)
 *
 * This function does NOT generate embeddings. All embeddings must be generated
 * by the ingestion pipeline (knowledgeStepRunner) before calling this function.
 *
 * Responsibilities:
 * 1. Verify embeddings exist in database
 * 2. Run integrity verification (blocking, feeds is_publishable flag)
 * 3. Run conflict detection (non-blocking)
 *
 * @param documentId - Document ID
 * @param chunks - Chunks to verify (embeddings should already exist in database)
 * @returns Success status
 */
export async function indexChunks(documentId: string, chunks: ChunkerChunk[]): Promise<boolean> {
  if (!documentId) {
    throw new Error('indexChunks: documentId required');
  }

  try {
    log('[KnowledgeIndex] Indexing', chunks.length, 'chunks for document:', documentId);
    log('[KnowledgeIndex] NOTE: PASSIVE MODE — Embeddings generated by knowledgeStepRunner');

    // Verify embeddings exist (generated by ingestion pipeline, not here)
    const { count, error: countError } = await supabase
      .from('knowledge_chunks')
      .select('id', { count: 'exact', head: true })
      .eq('document_id', documentId)
      .not('embedding_vector', 'is', null);

    if (countError) {
      warn('[KnowledgeIndex] Failed to verify embeddings:', countError.message);
    } else {
      log('[KnowledgeIndex] Verified embeddings exist:', count, 'chunks');
    }

    // Step 3: Integrity verification (blocking — result feeds is_publishable flag)
    log('[KnowledgeIndex] Starting integrity verification for document:', documentId);
    const integrityResult = await verifyAndPersistIntegrity(documentId);

    if (integrityResult.success) {
      log('[KnowledgeIndex] Integrity score:', integrityResult.report.integrityScore);
      log('[KnowledgeIndex] Publishable:', integrityResult.report.isPublishable);
    } else {
      warn('[KnowledgeIndex] Integrity verification failed:', integrityResult.error);
    }

    // Step 4: Conflict detection (non-blocking — errors must not fail ingestion)
    try {
      const conflictService = getKnowledgeConflictService();
      const conflictResult = await conflictService.detectKnowledgeConflicts(documentId);
      if (conflictResult.conflictsDetected > 0) {
        log('[KnowledgeIndex] Conflicts detected:', {
          documentId,
          count: conflictResult.conflictsDetected,
          timeMs: conflictResult.processingTimeMs,
        });
      }
    } catch (conflictError) {
      warn('[KnowledgeIndex] Conflict detection failed (non-blocking):', conflictError);
    }

    log('[KnowledgeIndex] Indexing complete for document:', documentId);
    return true;
  } catch (err) {
    error('[KnowledgeIndex] Indexing failed:', err);
    return false;
  }
}

// ---------------------------------------------------------------------------
// Cosine similarity helper
// ---------------------------------------------------------------------------

function cosineSimilarity(vecA: number[], vecB: number[]): number {
  if (vecA.length !== vecB.length) {
    throw new Error('Vectors must have same dimensions for cosine similarity');
  }

  let dotProduct = 0;
  let normA = 0;
  let normB = 0;

  for (let i = 0; i < vecA.length; i++) {
    dotProduct += vecA[i] * vecB[i];
    normA += vecA[i] * vecA[i];
    normB += vecB[i] * vecB[i];
  }

  const denominator = Math.sqrt(normA) * Math.sqrt(normB);
  return denominator === 0 ? 0 : dotProduct / denominator;
}

// ---------------------------------------------------------------------------
// Reranking helper
// ---------------------------------------------------------------------------

async function rerankChunks(
  query: string,
  chunks: { chunkId: string; content: string; similarity: number; documentId: string }[]
): Promise<Array<{ chunkId: string; content: string; similarity: number; documentId: string; rerankScore: number }>> {
  try {
    // Generate query embedding once
    const queryEmbedding = await generateEmbedding(query);
    if (!queryEmbedding) {
      throw new Error('Failed to generate query embedding for reranking');
    }

    // Batch embed all chunk contents
    const { embedBatch } = await import('./knowledgeEmbedding.service');
    const texts = chunks.map(c => c.content);
    const embeddings = await embedBatch(texts);

    if (!embeddings || embeddings.length !== chunks.length) {
      throw new Error('[KnowledgeIndex] Reranking batch size mismatch');
    }

    // Calculate rerank scores and sort
    return chunks
      .map((chunk, i) => ({
        ...chunk,
        rerankScore: cosineSimilarity(queryEmbedding, embeddings[i]),
      }))
      .sort((a, b) => b.rerankScore - a.rerankScore);
  } catch (err) {
    error('[KnowledgeIndex] Reranking failed:', err);
    // Return chunks with synthetic rerank scores if reranking fails
    return chunks.map(chunk => ({
      ...chunk,
      rerankScore: chunk.similarity,
    }));
  }
}

