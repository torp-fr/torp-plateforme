/**
 * Search for the most relevant chunks using hybrid search.
 * Combines pgvector cosine similarity (0.7 weight) with PostgreSQL full-text search (0.3 weight).
 * Delegates to the `match_knowledge_chunks` PostgreSQL function which computes hybrid scores.
 *
 * Requires migration 20260307000002_hybrid_rag_search.sql to be applied.
 * Function signature: match_knowledge_chunks(query_embedding vector(1536), query_text text, match_count int)
 */
export async function semanticSearch(
  query: string,
  limit: number = 10
): Promise<{ chunkId: string; content: string; similarity: number; documentId: string }[]> {
  try {
    log('[KnowledgeIndex] Hybrid search for:', query);

    // Generate query embedding for vector similarity
    const queryEmbedding = await generateEmbedding(query);
    if (!queryEmbedding) {
      throw new Error('Failed to generate query embedding');
    }

    // Hybrid search via pgvector + full-text search
    // Combines vector similarity (0.7) and BM25 FTS (0.3)
    const { data, error: rpcError } = await supabase.rpc('match_knowledge_chunks', {
      query_embedding: queryEmbedding,
      query_text: query,
      match_count: limit,
    });

    if (rpcError) {
      throw new Error(`Hybrid search RPC failed: ${rpcError.message}`);
    }

    const results = (data || []).map((row: {
      id: string;
      document_id: string;
      content: string;
      similarity: number;
    }) => ({
      chunkId: row.id,
      documentId: row.document_id,
      content: row.content,
      similarity: row.similarity,
    }));

    log('[KnowledgeIndex] Found', results.length, 'relevant chunks (hybrid search)');
    return results;
  } catch (err) {
    error('[KnowledgeIndex] Search failed:', err);
    return [];
  }
}

// ---------------------------------------------------------------------------
// getIndexStats
// ---------------------------------------------------------------------------

export async function getIndexStats(): Promise<IndexStats> {
  try {
    const { data: chunks, error: fetchError } = await supabase
      .from('knowledge_chunks')
      .select('embedding_vector');

    if (fetchError) {
      throw new Error(`Failed to get stats: ${fetchError.message}`);
    }

    const indexedChunks = (chunks || []).filter((c: any) => c.embedding_vector !== null).length;
    const totalChunks = chunks?.length || 0;

    return {
      chunksIndexed: indexedChunks,
      embeddingsGenerated: indexedChunks,
      // 384 dimensions × 4 bytes per float
      indexSize: totalChunks * 384 * 4,
      lastUpdated: new Date().toISOString(),
    };
  } catch (err) {
    error('[KnowledgeIndex] Failed to get stats:', err);
    return {
      chunksIndexed: 0,
      embeddingsGenerated: 0,
      indexSize: 0,
      lastUpdated: new Date().toISOString(),
    };
  }
}

// ---------------------------------------------------------------------------
// rebuildIndex
// ---------------------------------------------------------------------------

export async function rebuildIndex(documentId: string): Promise<boolean> {
  try {
    log('[KnowledgeIndex] Rebuilding index for document:', documentId);

    const { data: chunks, error: fetchError } = await supabase
      .from('knowledge_chunks')
      .select('*')
      .eq('document_id', documentId);

    if (fetchError || !chunks) {
      throw new Error(`Failed to fetch chunks: ${fetchError?.message}`);
    }

    const chunkerChunks = chunks.map((chunk: any) => ({
      content: chunk.content,
      tokenCount: chunk.token_count,
      startIndex: 0,
      endIndex: chunk.content.length,
    }));

    return await indexChunks(documentId, chunkerChunks);
  } catch (err) {
    error('[KnowledgeIndex] Rebuild failed:', err);
    return false;
  }
}

