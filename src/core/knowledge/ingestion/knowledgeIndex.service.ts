/**
 * Backfill embeddings for chunks where embedding_vector IS NULL.
 * Called when the ingestion pipeline fails to generate embeddings.
 *
 * @param documentId - Optional: if provided, only backfill for this document
 * @returns Count of embeddings regenerated
 */
export async function backfillEmbeddings(documentId?: string): Promise<number> {
  try {
    log('[KnowledgeIndex] Starting embedding backfill', documentId ? `for document ${documentId}` : 'for all documents');

    // Fetch all chunks with NULL embedding_vector
    let query = supabase
      .from('knowledge_chunks')
      .select('id, document_id, content, chunk_index')
      .is('embedding_vector', null);

    if (documentId) {
      query = query.eq('document_id', documentId);
    }

    const { data: nullChunks, error: fetchError } = await query;

    if (fetchError) {
      throw new Error(`Failed to fetch NULL embedding chunks: ${fetchError.message}`);
    }

    if (!nullChunks || nullChunks.length === 0) {
      log('[KnowledgeIndex] No chunks with NULL embeddings found');
      return 0;
    }

    log('[KnowledgeIndex] Found', nullChunks.length, 'chunks with NULL embeddings');

    // Batch regenerate embeddings with retry logic
    const BATCH_SIZE = 50; // Smaller batches to avoid timeout
    let successCount = 0;

    for (let i = 0; i < nullChunks.length; i += BATCH_SIZE) {
      const batch = nullChunks.slice(i, i + BATCH_SIZE);
      const contents = batch.map((c: any) => c.content);

      try {
        log(`[KnowledgeIndex] Backfilling batch ${Math.floor(i / BATCH_SIZE) + 1}/${Math.ceil(nullChunks.length / BATCH_SIZE)}`);

        // Generate embeddings in batch
        const embeddings = await generateEmbeddingBatch(contents);

        if (!embeddings || embeddings.length !== batch.length) {
          warn(`[KnowledgeIndex] Batch returned ${embeddings?.length ?? 0} embeddings, expected ${batch.length}`);
          continue;
        }

        // Update each chunk with its embedding
        for (let j = 0; j < batch.length; j++) {
          const chunk = batch[j];
          const embedding = embeddings[j];

          if (!embedding || embedding.length === 0) {
            warn(`[KnowledgeIndex] Skipping chunk ${chunk.id} — embedding is invalid`);
            continue;
          }

          const { error: updateError } = await supabase
            .from('knowledge_chunks')
            .update({ embedding_vector: embedding })
            .eq('id', chunk.id);

          if (updateError) {
            warn(`[KnowledgeIndex] Failed to update chunk ${chunk.id}:`, updateError.message);
          } else {
            successCount++;
          }
        }

        log(`[KnowledgeIndex] Batch complete — ${successCount}/${nullChunks.length} total updated`);
      } catch (batchError) {
        warn(`[KnowledgeIndex] Batch failed:`, batchError);
        // Continue with next batch instead of aborting
      }
    }

    log('[KnowledgeIndex] Backfill complete:', successCount, 'embeddings regenerated');
    return successCount;
  } catch (err) {
    error('[KnowledgeIndex] Backfill failed:', err);
    return 0;
  }
}

// Batch embedding helper
// Uses 384 dimensions to match current database schema (vector(384))
// Phase 42 migration will upgrade to 1536, but hasn't been applied yet
async function generateEmbeddingBatch(texts: string[]): Promise<number[][]> {
  const { data, error: fnError } = await supabase.functions.invoke(
    'generate-embedding',
    {
      body: { inputs: texts, model: 'text-embedding-3-small', dimensions: 384 },
    }
  );

  if (fnError) {
    throw new Error(`Edge Function error: ${fnError.message}`);
  }

  if (!data?.embeddings || !Array.isArray(data.embeddings)) {
    throw new Error('Invalid Edge Function response — missing embeddings array');
  }

  return data.embeddings as number[][];
}

// ---------------------------------------------------------------------------
// rebuildIndex
// ---------------------------------------------------------------------------

export async function rebuildIndex(documentId: string): Promise<boolean> {
  try {
    log('[KnowledgeIndex] Rebuilding index for document:', documentId);

    const { data: chunks, error: fetchError } = await supabase
      .from('knowledge_chunks')
      .select('*')
      .eq('document_id', documentId);

    if (fetchError || !chunks) {
      throw new Error(`Failed to fetch chunks: ${fetchError?.message}`);
    }

    const chunkerChunks = chunks.map((chunk: any) => ({
      content: chunk.content,
      tokenCount: chunk.token_count,
      startIndex: 0,
      endIndex: chunk.content.length,
    }));

    return await indexChunks(documentId, chunkerChunks);
  } catch (err) {
    error('[KnowledgeIndex] Rebuild failed:', err);
    return false;
  }
}

